func (it *SearchResultIterator) PageInfo() *iterator.PageInfo {
	return it.pageInfo
}

// Next returns the next result. Its second return value is iterator.Done if there are no more
// results. Once Next returns Done, all subsequent calls will return Done.
func (it *SearchResultIterator) Next() (*vectorsearchpb.SearchResult, error) {
	var item *vectorsearchpb.SearchResult
	if err := it.nextFunc(); err != nil {
		return item, err
	}
	item = it.items[0]
	it.items = it.items[1:]
	return item, nil
}

func (it *SearchResultIterator) bufLen() int {
	return len(it.items)
}

func (it *SearchResultIterator) takeBuf() interface{} {
	b := it.items
	it.items = nil
	return b
}
